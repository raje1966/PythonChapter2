# *FUNCTION DEFINITION

# Functions are used in order to achieve reusability and easy debugging.  Function declaration is not needed in Python programming.

# Function definition:  Every function should start with "def" keyword and every function should have a name, not equal to any keyword.

# parameters/arguments (optional)  This should be included in parenthesis.

# Every function name with or without argument should end with a (:) "colon".

# Every function return should return to its function call, which could be an empty or value return.  In Python multiple returns can be accepted.(multi-value returns can be done using tuples)

# * FUNCTION CALL:  Function name and no of arguments should be the same as in function definition.